uint64_t fb_pitch = 0;
uint16_t fb_bpp = 0;

inline void put_pixel(size_t x, size_t y, uint32_t color) {
    if (!fb_addr || !fb_pitch || !fb_bpp) return;
    if (x >= fb_width || y >= fb_height) return;

    uint32_t bytes_per_pixel = fb_bpp / 8;
    uint8_t *p = (uint8_t *)(fb_addr + y * fb_pitch + x * bytes_per_pixel);

    switch (fb_bpp) {
        case 32:
            *(uint32_t*)p = color;
            break;
        case 24:
            p[0] = color & 0xFF;
            p[1] = (color >> 8) & 0xFF;
            p[2] = (color >> 16) & 0xFF;
            break;
        case 16:
            *(uint16_t*)p = (uint16_t)color;
            break;
    }
}

void draw_char(size_t x, size_t y, char c, uint32_t color) {
    for (size_t i = 0; i < 8; i++) {
        uint8_t row = font8x8_basic[(uint8_t)c][i];
        for (size_t j = 0; j < 8; j++) {
            if (row & (1 << j)) put_pixel(x+j, y+i, color);
        }
    }
}

void draw_string(const char *str, uint32_t color) {
    while (*str) {
        if (*str == '\n') {         
            current_row += 8;       
            current_col = 0;        
        } else {
            if (current_col + 8 > fb_width) { 
                current_row += 8;
                current_col = 0;
            }
            draw_char(current_col, current_row, *str, color);
            current_col += 8;
        }
        str++;
    }
}

void draw_string_center(const char *str, uint32_t color) {
    const char *line_start = str;
    const size_t old_row = current_row;
    const size_t old_col = current_col;
    while (*line_start) {
        const char *line_end = line_start;
        while (*line_end && *line_end != '\n') line_end++;

        size_t line_len = line_end - line_start;   
        current_col = fb_width / 2 - (line_len * 8) / 2;

        for (const char *p = line_start; p < line_end; p++) {
            draw_char(current_col, current_row, *p, color);
            current_col += 8;
        }

        current_row += 8; 
        if (*line_end == '\n') 
            line_start = line_end + 1; 
        else 
            break;
    }
    current_col = old_col;
}

void draw_console_time(const char *time, uint32_t color){
    size_t col = fb_width - 64;              

    while (*time) {
        draw_char(col, current_row, *time, color);
        col += 8; 
        time++;
    }
}

void console_putc(char c, uint32_t color) {
    if (c == '\n') {
        current_row += 8;
        current_col = 0;
        return;
    }
    if (c == '\b') {
        if (current_col >= 8) {
            current_col -= 8;
        }
        draw_char(current_col, current_row, ' ', color);
        return;
    }
    if (current_col + 8 > fb_width) {
        current_row += 8;
        current_col = 0;
    }
    draw_char(current_col, current_row, c, color);
    current_col += 8;
}

void framebuffer_init(void) {
    struct limine_framebuffer_response *fb_resp =
        limine_framebuffer_request.response;

    if (!fb_resp || fb_resp->framebuffer_count < 1)
        return;

    struct limine_framebuffer *fb =
        fb_resp->framebuffers[0];

    fb_addr   = (uintptr_t)fb->address;
    fb_pitch  = fb->pitch;
    fb_width  = fb->width;
    fb_height = fb->height;
    fb_bpp    = fb->bpp;
    char height[32];
    char width[32];
    
    utoa(fb_height, height);
    utoa(fb_width, width);
    
    // draw_string(height, 0xFFFFFF);
    // draw_string(" ", 0xFFFFFF);
    // draw_string(width, 0xFFFFFF);
    // draw_string(" ", 0xFFFFFF);
    
}

uint64_t framebuffer_width(void) {
    return fb_width;
}

uint64_t framebuffer_height(void) {
    return fb_height;
}
